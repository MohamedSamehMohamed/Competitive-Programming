int v1 , v2 , t , d ;
int Sum  ;

int main ()
{
    v1 = ReadInt() ;
    v2 = ReadInt() ;
    t  = ReadInt() ;
    d  = ReadInt() ;
    
    int A [ t ] ;
    A [0] = v1 ;
    for (int i =1 ; i  < t ; i++)
        A [ i ] = A [i-1] + d ;
    if (  A[t-1] != v2  )
    {
        A [ t-1 ] = v2 ;
        for (int i = t-2 ; i >= 0 ; i--)
        {
            if ( abs ( A [ i ] - A [ i + 1 ] ) <= d  )
                break ;
            bool p = (  A[i] >= A[ i + 1 ] ? 1 : 0 );
            while (abs ( A [ i ] - A [ i + 1 ] ) >  d)
            {
                if (p)
                    A[i]--;
                else
                    A[i]++;

            }
        }
    }

    for (int i =0 ; i < t; i ++)
        Sum+= A[i];
    cout << Sum <<"\n";

return 0 ;
}